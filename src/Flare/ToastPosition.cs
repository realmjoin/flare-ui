namespace Flare;

/// <summary>
/// Screen position where toast notifications are displayed.
/// </summary>
public enum ToastPosition
{
    /// <summary>Top-right corner of the viewport.</summary>
    TopRight,

    /// <summary>Top-left corner of the viewport.</summary>
    TopLeft,

    /// <summary>Bottom-right corner of the viewport.</summary>
    BottomRight,

    /// <summary>Bottom-left corner of the viewport.</summary>
    BottomLeft,

    /// <summary>Top-center of the viewport.</summary>
    TopCenter,

    /// <summary>Bottom-center of the viewport.</summary>
    BottomCenter
}
